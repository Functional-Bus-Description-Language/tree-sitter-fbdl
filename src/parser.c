static inline bool sym_bit_literal_character_set_1(int32_t c) {
  return (c < 'Z'
    ? (c < 'H'
      ? (c < '0'
        ? c == '-'
        : (c <= '9' || (c >= 'A' && c <= 'F')))
      : (c <= 'H' || (c < 'U'
        ? c == 'L'
        : (c <= 'U' || (c >= 'W' && c <= 'X')))))
    : (c <= 'Z' || (c < 'u'
      ? (c < 'h'
        ? (c >= 'a' && c <= 'f')
        : (c <= 'h' || c == 'l'))
      : (c <= 'u' || (c < 'z'
        ? (c >= 'w' && c <= 'x')
        : c <= 'z')))));
}

